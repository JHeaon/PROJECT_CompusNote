function fontBold() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.fontWeight = '700';
}
function fontUnder() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.textDecoration = 'underline';
}
function fontItalic() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.fontStyle = 'italic';
}
function fontCancel() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.textDecoration = 'line-through';
}

function alignBoth() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.textAlign = 'justify';
}
function alignLeft() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.textAlign = 'left';
}
function alignCenter() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.textAlign = 'center';
}
function alignRight() {
    let txtvalue = document.getElementById('txtarea');
    txtvalue.style.textAlign = 'right';
}